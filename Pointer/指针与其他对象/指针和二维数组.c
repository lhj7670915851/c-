#include<stdio.h>
#include<string.h>
int main()
{
    int a[3][4]={1,3,5,7,9,11,2,4,6,8,10,12};
    int i,j;
    int *p;
    p=a[0];
    printf("/******************************************************************************/\n");
    for(i=0;i<3;i++)
        for(j=0;j<4;j++)
            printf("a[%d][%d]=%d\n,i,j,a[i][j]");
    printf("/******************************************************************************/\n");
    printf("&a[0][0]的值%p\n",&a[0][0]);
    printf("a[0]的值%p\n",a[0]);
    printf("/******************************************************************************/\n");
    printf("a[0][1]的值%d\n",a[0][1]);
    printf("a[1][1]的值%d\n",a[1][1]);
    printf("/******************************************************************************/\n");
    printf("a的值%p\n",a);
    printf("&a[0]的值%p\n",&a[0]);
    printf("a+1的值%p\n",a+1);
    printf("&a[1]的值%p\n",&a[1]);
    printf("/******************************************************************************/\n");
    printf("a[0][1]的值%d\n",*((*a+1)));
    printf("a[1][1]的值%d\n",*((*(a+1)+1)));
    printf("/******************************************************************************/\n");
    printf("int的大小=%d\n",sizeof(int));
    printf("a[0][0]的大小=%d\n",sizeof(a[0][0]));
    printf("a的大小=%d\n",sizeof(a));
    printf("a[0]的大小=%d\n",sizeof(a[0]));
    printf("/******************************************************************************/\n");
    printf("a[0][1]的值%d\n",*(p+1));
    printf("a[1][0]的值%d\n",*(p+4));
    printf("/******************************************************************************/\n");
    for(i=0;i<3;i++)
        for(j=0;j<4;j++)
            printf("a[%d][%d]=%d\n,i,j,a[i][j]");
    printf("/******************************************************************************/\n");
}